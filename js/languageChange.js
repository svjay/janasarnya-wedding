function getText(lang, key) {
    const keys = key.split('.');
    let text = language[lang];
    // console.log("text", text);
    // Traverse the nested object using the keys
    for (const k of keys) {
        text = text[k];
        if (!text) {
            return null; // Return null if any key is not found
        }
    }

    return text || '';

}


